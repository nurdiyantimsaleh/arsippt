		<p align="center"><strong>	Tugas dan Kewenangan Pengadilan Tinggi</strong></p> <br>
<i class="fa fa-arrow-right"></i>Pengadilan Tinggi bertugas dan berwenang mengadili perkara pidana dan perkara perdata di tingkat banding.<br><br>
<i class="fa fa-arrow-right"></i> Pengadilan Tinggi bertugas dan berwenang mengadili di tingkat pertama dan terakhir sengketa kewenangan mengadili antara Pengadilan Negeri di daerah hukumnya.<br><br>
           (Pasal 51 Undang-Undang No. 2 Tahun 1986 Tentang Peradilan Umum, yang telah diubah pertama dengan Undang-Undang No. 8 Tahun 2004, kedua dengan Undang-Undang No. 49 Tahun 2009).<br><br>
		   <i class="fa fa-arrow-right"></i>Pengadilan Tinggi dapat memberikan keterangan, pertimbangan dan nasehat tentang hukum instansi pemerintah di daerah apabila diminta.<br><br>
(Pasal 52 Undang-Undang No. 2 Tahun 1986 Tentang Peradilan Umum, yang telah diubah pertama dengan Undang-Undang No. 8 Tahun 2004, kedua dengan Undang-Undang No. 49 Tahun 2009).<br><br>
<i class="fa fa-arrow-right"></i> Pengadilan wajib memberikan akses kepada masyarakat untuk memperoleh informasi yang berkaitan dengan putusan dan biaya perkara dalam proses persidangan.<br><br>
<i class="fa fa-arrow-right"></i> Pengadilan wajib menyatakan salinan putusan kepada para pihak dalam jangka waktu paling lambat 14 (empat belas) hari kerja sejak putusan diucapkan.<br><br>
(Pasal 52a Undang-Undang No. 2 Tahun 1986 Tentang Peradilan Umum, yang telah diubah pertama dengan Undang-Undang No. 8 Tahun 2004, kedua dengan Undang-Undang No. 49 Tahun 2009).  

