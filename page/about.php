PENGADILAN TINGGI MANADO	<br>	<br>
Pengadilan Tinggi Manado terbentuk berdasarkan Undang-Undang No. 12 Tahun 1968 tanggal 26 Nopember 1968. Pengadilan Tinggi Manado secara resmi beroperasi pada tanggal 22 Januari 1969 yang ditandai dengan dilantiknya BapakSaldiman Wirjatmo, SH sebagai Ketua Pengadilan Tinggi Manado oleh Bapak Menteri Kehakiman RI Prof. Oemar Seno Adji, SH disaksikan oleh Hakim Agung RI Bapak Indroharto, SH dan Ketua Pengadilan Tinggi Makassar Bapak R. Soenarto, SH. Oleh karena itu pada setiap tanggal 22 Januari diperingati sebagai hari ulang tahun Pengadilan Tinggi Manado. Pada waktu itu Pengadilan Tinggi Manado untuk sementara berkantor bersama dengan Pengadilan Negeri Manado di Jl. Sam Ratulangi No. 18. Setelah selesai pembangunan gedung kantor yang baru yang terletak di Jl. Sam Ratulangi No. 20 dan setelah diresmikan oleh Menteri Kehakiman RI, Prof. Oemar Seno Adji, SH maka pada tanggal 8 September 1972 baru menempati gedung kantor yang baru sampai sekarang.	<br>
Adapun Ketua Pengadilan Tinggi Manado yang pertama adalah Saldiman Wirjatmo, SH., Wakil Ketua Olden Bidara, SH., dan Hakim Tinggi, J.N. Jassin sedangkan Panitera/Sekretaris, Drs. F. Tidajoh. Pegawai pertama waktu itu J. Gerungan, P.A. Tulandi, R.C.L. Oroh, Ny. Rahmaniah Madjid, Karel Tumbel, Benny Irot, Ny. Siwu dan W. Kandouw kemudian setelah menempati gedung yang baru menyusul Max Leuwol, E. Ratulangi dan Idris Madina, SH.	<br>
 	<br>	<br>
